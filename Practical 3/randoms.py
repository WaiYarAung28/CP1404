import random

print(random.randint(5, 20))  # line 1
# I see 7 on line 1 after the output.
# The smallest number I could have seen is 5 and the largest number I could have seen is 20.

print(random.randrange(3, 10, 2))  # line 2
# I see 7 on line 2 after the output.
# The smallest number I could have seen is 3 and the largest number I could have seen is 9.
# The line couldn't produce a 4.

print(random.uniform(2.5, 5.5))  # line 3
# I see 2.645893729244611 on line 3 after the output.
# The smallest number I could have seen is 2.5 and largest number I could have seen is 5.5.

print(random.randrange(1, 100))
# Random number between 1 and 100 inclusive.
